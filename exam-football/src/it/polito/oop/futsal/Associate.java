package it.polito.oop.futsal;

public class Associate {
    private String first;
    private String last;
    private String mobile;

    public Associate(String first, String last, String mobile) {
        this.first = first;
        this.last = last;
        this.mobile = mobile;
    }

    public String getFirst() {
        return first;
    }
    public String getLast() {
        return last;
    }
    public String getMobile() {
        return mobile;
    }


    
    
}
